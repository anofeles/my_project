<?php


namespace TestCMS\Models;


use Dimsav\Translatable\Translatable;
use Illuminate\Database\Eloquent\Model;
use Ramsey\Uuid\Uuid;

class LewsgadTest extends Model
{
    use Translatable;
    protected $table = 'lewsgad_test';
    public $translationModel = 'TestCMS\Models\LewsgadTestTranslations';
    public $translatedAttributes = [
        'title', 'desc', 'locale','defic'
    ];
    protected $fillable = [
        'uuid','row','time','timetest','proc'
    ];
    public function Lewsgadtest($uuid)
    {
//        dump($uuid);
        return $this->belongsToMany(LewsgadTest::class, 'lewsgad_test_to_any', 'empti', 'lewsgad_test', 'uuid', 'uuid')
            ->withPivot('row_uuid','lewsgad_test')->where('lewsgad_test', '=', $uuid);
    }
    public static function boot()
    {
        parent::boot(); // TODO: Change the autogenerated stub
        self::creating(function ($model) {
            $model->uuid = Uuid::uuid4();
        });
    }
}
